<h2>
Simplicity & Flexibility
</h2>
SimpleApp is an open source <a target="_blank"  href="http://www.php.net">PHP</a> framework capable of accomodating designers with little to no programming experience as well as seasoned programmers looking for a lightweight, simple, and flexible solution to build on. Although SimpleApp fits the mold of a web/application framework, it also provides out-of-the-box built in features more typically associated with content management systems.  Features include:
<ul class="features">
	<li>
    <span>Page protection/authentication</span> with support for three generic groups: super administrator, administrator, and general member.  
    </li>
    <li>
    <span>Internationalization/Multi-language</span> support with built in mechanism to allow on-the-fly switching.
    </li>
    <li>
    <span>Page caching</span> which can be set on a per-page basis.
    </li>
    <li>
    <span>Search Engine Friendly</span> urls as well as support for query strings.  Query strings can be used in conjunction with the SEO friendly urls or exclusively (useful for folks with restrictive hosting plans which do not provide .htaccess support).
    </li>
    <li>
    <span>Templating system</span> which is flexible and easy to use.
    </li>
    <li>
    <span>Administrative back-end</span> for creating & managing pages & users, easily managing configuration settings, and more.
     </li>
     <li>
     <span>Database not required</span> to run the basic install, however, support for MySQL is included.
</ul> 
